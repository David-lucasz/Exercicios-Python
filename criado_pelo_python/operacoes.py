n1 = input("digite um numero: ")
n2 = input("digite outro numero: ")

# para realizar as operaçoes aritmedicas, sera
# necessarios converter as variaveis n1 e n2 em 
# tipos numericos, que podem ser: int uo float

n1 = int(n1)
n2 = int(n2)

soma = n1 + n2 
subtrair = n1 - n2
multiplicar = n1 * n2
dividir = n1 / n2

print(f"a soma entre os numeros {n1} e {n2} é {soma}")
print(f"a subtraçao entre os numeros {n1} e {n2} {subtrair}")
print(f" a multiplicaçao entre os numeros {n1} e {n2} {multiplicar}")
print(f" a divisao entre os numeros {n1} e {n2} {dividir}")
      
      